#include "libft.h"

/*
 * Description:
 *
 * Returns a pointer to the last occurence of the character c in the string s.
 */

char	*ft_strrchr(const char *s, int c)
{
	int	s_len;

	s_len = ft_strlen(s);
	while (s_len >= 0)
	{
		if (*(s + s_len) == (unsigned char)c)
			return ((char *)s + s_len);
		s_len--;
	}
	return (NULL);
}
