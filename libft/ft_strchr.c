#include "libft.h"

/*
 * Description:
 *
 * Returns a pointer to the first occurrence of the character c in the string s.
 */

char	*ft_strchr(const char *s, int c)
{
	while (*s && *s != (unsigned char)c)
		s++;
	if (*s == (unsigned char)c)
		return ((char *)s);
	return (NULL);
}
